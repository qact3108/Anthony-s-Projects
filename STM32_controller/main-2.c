void ACCELERO_IO_Read (uint8_t *buffer, uint8_t address, uint16_t count)	{

	uint8_t control_byte;

	if (count == 1) {

		control_byte = (address) | 0x80;

	} else {

		control_byte = (address) | 0xC0;

	}


    HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, GPIO_PIN_RESET);

    SPIx_WriteRead(control_byte);

    // Read the data
    for (uint16_t i = 0; i < count; i++) {
        buffer[i] = SPIx_WriteRead(0);  // Send dummy byte to receive data
    }

    // Set chip select (PE3) high to end communication
    HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, GPIO_PIN_SET);

}

