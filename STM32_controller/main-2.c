int16_t getActualValue(uint8_t lowByte, uint8_t highByte) {
  int16_t result = (int16_t)(highByte << 8) + lowByte;

  if (result & 0x8000) {
      // If the sign bit is set (negative number)
      result = -(~result + 1);
  }

  return result*0.006;
}

void objectiveTwo() {
	  mappedPitch = abs(pitch*1700/90);
	  mappedRoll = abs(roll*1700/90);


	  TIM4->CCR2 = mappedPitch;
	  TIM4->CCR3 = mappedRoll;

	  if (pitch > 0) {
		 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_7));
		 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);
	  } else if (pitch < 0) {
	 	 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_7));
	 	 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);
	  } else {
		 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);
		 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);
	  }

	 if (roll > 0) {
	 	 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_8));
	 	 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_RESET);
	 } else if (roll < 0) {
	 	 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_8));
	 	 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_RESET);
	 } else {
	 	 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_RESET);
	 	 HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_RESET);
	 }



}

void get_sineval () {

	floatRoll = ((180*atan2(y,sqrt(x*x+z*z))/PI+90) / 180);

	for (int i = 0; i < 100; i++)	{
		 sine_val[i] = ((floatRoll)*(sin(i*2*PI/100)+1)*(4096/2));
	}
}

void objectiveThree() {

	  get_sineval();
	  HAL_DAC_Start_DMA(&hdac, DAC_CHANNEL_1, sine_val, 100, DAC_ALIGN_12B_R);
	  __HAL_TIM_SET_PRESCALER(&htim2,((pitch+90)*(84-1)/180));

}
